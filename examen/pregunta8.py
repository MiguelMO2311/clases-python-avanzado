# ✅ La opción correcta es:
# A) Suelen ser económicas y una buena opción para empezar prácticamente 
# cualquier proyecto desde cero.
# Simplicidad inicial: todo está en un solo lugar, lo que facilita el desarrollo, pruebas y despliegue.

# Coste bajo: menos infraestructura, menos complejidad operativa.

# Ideal para MVPs y proyectos pequeños: permite validar ideas rápidamente sin sobreingeniería.


# Por qué las otras opciones no son correctas
# B) Incorrecta: se pueden aplicar métricas, logging y observabilidad perfectamente en monolitos (ej. Prometheus, Grafana, APMs).

# C) Incorrecta: los monolitos no son fácilmente escalables, ya que escalar una parte implica replicar todo el sistema.

# D) Incorrecta y exagerada: aunque tienen limitaciones (modularidad, escalabilidad, resiliencia), no se desaconsejan en todos los casos. De hecho, muchas empresas comienzan con monolitos y migran gradualmente.